import java.util.Scanner;

/**
 *
 * @author aluno
 */
public class Multiplicacao {

    public static void main(String[] args) {
        int numero = 1;
        int tabuada = 10;
        while (numero <= 10) {
            System.out.println(numero + " X "+tabuada+" = " + numero * tabuada);
            numero++;            
        }

    }

}
